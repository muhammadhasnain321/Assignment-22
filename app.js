// // Function to display a message in the browser
function displayMessage(message) {
    document.write(message + "<br>");
}

// Function to simulate a dice roll using Math.random()
function rollDice() {
    return Math.floor(Math.random() * 6) + 1;
}

// Function to simulate a coin toss using Math.random()
function tossCoin() {
    return Math.random() < 0.5 ? "Heads" : "Tails";
}

// 1. Takes a positive integer from the user and displays round off, floor, and ceil values
let posNum = parseInt(prompt("Enter a positive integer:"));
displayMessage(`Number: ${posNum}`);
displayMessage(`Round off value: ${Math.round(posNum)}`);
displayMessage(`Floor value: ${Math.floor(posNum)}`);
displayMessage(`Ceil value: ${Math.ceil(posNum)}`);

// 2. Takes a negative floating point number from the user and displays round off, floor, and ceil values
let negFloat = parseFloat(prompt("Enter a negative floating-point number:"));
displayMessage(`Number: ${negFloat}`);
displayMessage(`Round off value: ${Math.round(negFloat)}`);
displayMessage(`Floor value: ${Math.floor(negFloat)}`);
displayMessage(`Ceil value: ${Math.ceil(negFloat)}`);

// 3. Displays the absolute value of a number
let absNum = parseFloat(prompt("Enter a number to find its absolute value:"));
displayMessage(`Absolute value of ${absNum}: ${Math.abs(absNum)}`);

// 4. Simulates a dice roll using random() method and displays the value
let diceValue = rollDice();
displayMessage(`Dice roll value: ${diceValue}`);

// 5. Simulates a coin toss using random() method and displays the result
let coinResult = tossCoin();
displayMessage(`Coin toss result: ${coinResult}`);

// 6. Shows a random number between 1 and 100 in the browser
let randomNum = Math.floor(Math.random() * 100) + 1;
displayMessage(`Random number between 1 and 100: ${randomNum}`);

// 7. Takes user's weight input and displays it
let userWeight = prompt("Enter your weight (e.g., 50, 50kgs, 50.2kgs, 50.2kilograms):");
let parsedWeight = parseFloat(userWeight);
displayMessage(`Your weight is: ${parsedWeight} kg`);

// 8. Stores a random secret number from 1 to 10 and asks the user to guess the number
let secretNum = Math.floor(Math.random() * 10) + 1;
let userGuess = parseInt(prompt("Guess a number between 1 and 10:"));
if (userGuess === secretNum) {
    displayMessage("Congratulations! You guessed the correct number.");
} else {
    displayMessage(`Sorry, the correct number was ${secretNum}. Better luck next time!`);
}
