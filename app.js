// Chapter 30: Controlling the length of decimals

// Rounding a number to 4 decimal places and converting it to a string
let num = 5.123456789;
let roundedTo4Places = num.toFixed(4).toString();
console.log(`Rounded to 4 decimal places and converted to string: ${roundedTo4Places}`);

// Rounding a number to 2 decimal places, converting it to a string, converting it back to a number
num = num.toFixed(2);
num = parseFloat(num);
console.log(`Rounded to 2 decimal places and back to number: ${num}`);

// Testing if a number rounded to 2 digits and converted to a string has more than 4 characters
let testNum = 5.123456789;
let roundedTo2Digits = testNum.toFixed(2).toString();
if (roundedTo2Digits.length > 4) {
    console.log(`The rounded number has more than 4 characters: ${roundedTo2Digits}`);
} else {
    console.log(`The rounded number does not have more than 4 characters: ${roundedTo2Digits}`);
}

